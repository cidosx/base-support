<?php

namespace Const;

/**
 * 正则表达式
 */
class Regex {

	/**
	 * 年月日正则 ( xxxx-xx-xx )
	 * @var string
	 */
	CONST YMD = '/^((((19|20)\d{2})-(0[13-9]|1[012])-(0[1-9]|[12]\d|30))|(((19|20)\d{2})-(0[13578]|1[02])-31)|(((19|20)\d{2})-02-(0[1-9]|1\d|2[0-8]))|((((19|20)([13579][26]|[2468][048]|0[48]))|(2000))-02-29))$/';

	/**
	 * 宽松的年月日正则 ( 这种允许 xxxx-x-x 格式的存在 )
	 * @var string
	 */
	CONST YMD_LAX = '/^((((19|20)\d{2})-(0?[13-9]|1[012])-(0?[1-9]|[12]\d|30))|(((19|20)\d{2})-(0?[13578]|1[02])-31)|(((19|20)\d{2})-0?2-(0?[1-9]|1\d|2[0-8]))|((((19|20)([13579][26]|[2468][048]|0[48]))|(2000))-0?2-29))$/';

	/**
	 * 年月日时分秒 ( 2016-04-25 13:29:20 )
	 */
	CONST YMDHIS = '/^((((19|20)\d{2})-(0[13-9]|1[012])-(0[1-9]|[12]\d|30))|(((19|20)\d{2})-(0[13578]|1[02])-31)|(((19|20)\d{2})-02-(0[1-9]|1\d|2[0-8]))|((((19|20)([13579][26]|[2468][048]|0[48]))|(2000))-02-29))\ (((0[0-9])|(1[0-9])|(2[0-3])):([0-5][0-9])((s)|(:([0-5][0-9]))))?$/';

	/**
	 * 身份证 18位
	 * @var  string
	 */
	CONST ID_CARD = '/^[1-9]\d{5}[1-9]\d{3}((0\d)|(1[0-2]))(([0|1|2]\d)|3[0-1])\d{3}(\d|x|X)$/';

	/**
	 * 中英文标点（unicode编码）
	 * 定位符可根据需要在程序中灵活补全
	 */
	CONST PUNCTUATION_UNICODE = '(%7E|%60|%21|%40|%23|%24|%25|%5E|%26|%27|%2A|%28|%29|%2B|%7C|%5C|%3D|\-|_|%5B|%5D|%7D|%7B|%3B|%22|%3A|%3F|%3E|%3C|%2C|\.|%2F|%A3%BF|%A1%B7|%A1%B6|%A1%A2|%A1%A3|%A3%AC|%7D|%A1%B0|%A3%BA|%A3%BB|%A1%AE|%A1%AF|%A1%B1|%A3%FC|%A3%BD|%A1%AA|%A3%A9|%A3%A8|%A1%AD|%A3%A4|%A1%A4|%A3%A1|%E3%80%82|%EF%BC%81|%EF%BC%8C|%EF%BC%9B|%EF%BC%9F|%EF%BC%9A|%E3%80%81|%E2%80%A6%E2%80%A6|%E2%80%9D|%E2%80%9C|%E2%80%98|%E2%80%99)';
}
